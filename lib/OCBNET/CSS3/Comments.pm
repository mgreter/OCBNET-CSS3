package OCBNET::CSS3::Comment;
####################################################################################################

use strict;
use warnings;

####################################################################################################
use base 'OCBNET::CSS3';
####################################################################################################

# static getter
#**************************************************************************************************
sub type { return 'text' }

####################################################################################################

# load regex for comments
#**************************************************************************************************
use OCBNET::CSS3::Regex::Base qw($re_comment);

# add basic extended type with highest priority
#**************************************************************************************************
push @OCBNET::CSS3::types, [
	qr/\A\s*$re_comment\s*\z/is,
	'OCBNET::CSS3::Comment'
];

####################################################################################################
####################################################################################################
1;
